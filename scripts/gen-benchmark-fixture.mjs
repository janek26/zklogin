#!/usr/bin/env node
/**
 * Generates a self-contained benchmark fixture for proof-generation timing.
 *
 * Because we cannot sign with Google's private key, this script:
 * 1. Generates a fresh RSA-2048 key pair.
 * 2. Reuses a kid from the frozen Google JWK snapshot.
 * 3. Replaces that key's entry with our public key (same limb format).
 * 4. Signs a valid RS256 JWT with our private key.
 * 5. Computes the account_id and public_key_hash the same way the SDK does.
 *
 * The resulting proof is cryptographically valid (the Noir circuit verifies
 * the RSA signature against our public key) but will NOT verify on-chain
 * (the JWK Merkle root differs from the deployed root). That is fine for
 * benchmarking proof-generation speed.
 */
import { writeFile, mkdir } from 'node:fs/promises'
import { generateKeyPairSync, createSign } from 'node:crypto'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { createRequire } from 'node:module'

const __dirname = dirname(fileURLToPath(import.meta.url))
const require = createRequire(join(__dirname, '..', 'package.json'))

// Resolve pnpm-hoisted packages
const paramgenPath = join(__dirname, '..', 'node_modules/.pnpm/@mach-34+noir-bignum-paramgen@1.1.0/node_modules/@mach-34/noir-bignum-paramgen/dist/index.js')
const bbjsPath = join(__dirname, '..', 'node_modules/.pnpm/@aztec+bb.js@0.65.2/node_modules/@aztec/bb.js/dest/node/index.js')
const snapshotPath = join(__dirname, '..', 'apps/web/src/generated/jwk-snapshot.json')

const { bnToLimbStrArray, bnToRedcLimbStrArray } = require(paramgenPath)
const { BarretenbergSync, Fr } = await import(bbjsPath).then(m => m.default || m)

// --- Read the frozen snapshot to reuse a kid ---
const snapshot = JSON.parse(await readFile(snapshotPath))
const originalKey = snapshot.keys[0]
const kid = originalKey.kid
const authProviderId = originalKey.authProviderId

// --- Generate RSA-2048 key pair ---
const { privateKey, publicKey } = generateKeyPairSync('rsa', { modulusLength: 2048 })
const jwk = publicKey.export({ format: 'jwk' })
const n = BigInt('0x' + Buffer.from(jwk.n, 'base64url').toString('hex'))

// --- Compute limbs (same as PublicKeyRegistry) ---
const public_key_limbs = bnToLimbStrArray(n)
const public_key_redc_limbs = bnToRedcLimbStrArray(n)

// --- Compute public key hash via Pedersen (same as PublicKeyRegistry) ---
const api = BarretenbergSync.getSingleton()
const hashInput = [...public_key_limbs, ...public_key_redc_limbs].map(x => new Fr(BigInt(x)))
const hashFr = await api.pedersenHash(hashInput, 0)
const publicKeyHash = hashFr.toString()

// --- Compute account_id (same as ZkLoginSdk.getAccountIdFromJwt) ---
const GOOGLE_CERTS_URL = 'https://www.googleapis.com/oauth2/v3/certs'
const GOOGLE_CLIENT_ID = 'benchmark-client-id'
const JWT_SUB = 'benchmark-subject-123456'
const JWT_AUD = GOOGLE_CLIENT_ID
const JWT_SUB_MAX_LEN = 64
const JWT_AUD_MAX_LEN = 256

// noirPackBytes: pack bytes into 31-byte field elements
function noirPackBytes(arr) {
  const padded = [...arr]
  while (padded.length % 31 !== 0) padded.push(0)
  const res = []
  for (let i = 0; i < padded.length; i += 31) {
    let val = 0n
    let offset = 1n
    for (let j = 0; j < 31; j++) {
      val += BigInt(padded[i + j] ?? 0) * offset
      offset *= 256n
    }
    res.push(val)
  }
  return res
}

function arrayPadEnd(arr, len, val) {
  const out = [...arr]
  while (out.length < len) out.push(val)
  return out
}

const subBytes = Array.from(Buffer.from(JWT_SUB))
const audBytes = Array.from(Buffer.from(JWT_AUD))
const salt = Fr.ZERO
const accountIdFr = await api.pedersenHash([
  ...noirPackBytes(arrayPadEnd(subBytes, JWT_SUB_MAX_LEN, 0)),
  ...noirPackBytes(arrayPadEnd(audBytes, JWT_AUD_MAX_LEN, 0)),
  salt,
], 0)
const accountId = accountIdFr.toString()

// --- Build the benchmark JWK snapshot (replace the first key) ---
const benchKey = {
  authProviderId,
  kid,
  limbs: { public_key_limbs, public_key_redc_limbs },
  hash: publicKeyHash,
  jwkProof: originalKey.jwkProof, // not used by the prover, only for on-chain registration
}
const benchSnapshot = {
  ...snapshot,
  keys: [benchKey, ...snapshot.keys.slice(1)],
  benchmarkNote: 'Generated by gen-benchmark-fixture.mjs — not for on-chain use',
}

// --- Sign a valid JWT ---
const now = Math.floor(Date.now() / 1000)
const header = { alg: 'RS256', kid, typ: 'JWT' }
const payload = {
  iss: 'https://accounts.google.com',
  aud: GOOGLE_CLIENT_ID,
  sub: JWT_SUB,
  iat: now,
  exp: now + 3600,
  nonce: 'a'.repeat(64), // circuit requires exactly 64-byte nonce array
}

function base64Url(input) {
  return Buffer.from(input).toString('base64url')
}

const headerB64 = base64Url(JSON.stringify(header))
const payloadB64 = base64Url(JSON.stringify(payload))
const signingInput = `${headerB64}.${payloadB64}`

const signer = createSign('RSA-SHA256')
signer.update(signingInput)
const signature = signer.sign(privateKey)
const signatureB64 = base64Url(signature)

const jwt = `${headerB64}.${payloadB64}.${signatureB64}`

// --- Write the fixture ---
const fixtureDir = join(__dirname, '..', 'apps/web/src/generated')
const fixture = {
  jwt,
  expectedNonce: payload.nonce,
  accountId,
  publicKeyHash,
  jwkSnapshot: benchSnapshot,
  generatedAt: new Date().toISOString(),
}

await writeFile(join(fixtureDir, 'benchmark-fixture.json'), JSON.stringify(fixture, null, 2) + '\n')
console.log('Benchmark fixture written to apps/web/src/generated/benchmark-fixture.json')
console.log(`  kid: ${kid}`)
console.log(`  publicKeyHash: ${publicKeyHash}`)
console.log(`  accountId: ${accountId}`)
console.log(`  jwt length: ${jwt.length}`)

// --- Helper: readFile (hoisted) ---
async function readFile(path) {
  return (await import('node:fs/promises')).readFile(path, 'utf8')
}
